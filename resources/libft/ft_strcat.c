#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*tmp;
	int		i;

	tmp = s1;
	i = 0;
	while (tmp[i])
		i++;
	while (*s2)
	{
		tmp[i] = *s2;
		i++;
		s2++;
	}
	tmp[i] = '\0';
	return (s1);
}
